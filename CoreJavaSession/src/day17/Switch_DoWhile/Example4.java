package day17.Switch_DoWhile;

public class Example4 {

	public static void main(String[] args) {
		System.out.println("Program Starts");
		for (char ch = 'a'; ch <= 'z'; ch++) {
			System.out.print(ch + " ");
		}
		System.out.println();
		for (char ch = 'Z'; ch >= 'A'; ch--) {
			System.out.print(ch + " ");
		}
		System.out.println();
		System.out.println("Program Ends");
	}
}
